-- DROP TABLES IF THEY EXIST -------------------------------------------------------------------------------------------

DECLARE
  PROCEDURE dropTable(tableName IN VARCHAR2) IS
    BEGIN
      EXECUTE IMMEDIATE 'DROP TABLE ' || tableName;
      EXCEPTION WHEN OTHERS
      THEN NULL;
    END;
BEGIN
  dropTable('MovieGenre');
  dropTable('MovieCast');
  dropTable('PersonPlays');
  dropTable('Genre');
  dropTable('Movie');
  dropTable('MovieCharacter');
  dropTable('Person');
END;
