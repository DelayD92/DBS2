DECLARE
    PROCEDURE dropTable(tableName IN VARCHAR2) IS
        TABLE_DOES_NOT_EXISTS EXCEPTION;
        PRAGMA EXCEPTION_INIT (TABLE_DOES_NOT_EXISTS, -00942);
    BEGIN
        EXECUTE IMMEDIATE 'DROP TABLE ' || tableName || ' CASCADE CONSTRAINTS';
    EXCEPTION
        WHEN TABLE_DOES_NOT_EXISTS THEN NULL;
      --WHEN OTHERS THEN NULL -- Don't use this, to see if there is any other issue while dropping the tables
    END;
BEGIN
    dropTable('MovieGenre');
    dropTable('PersonPlays');
    dropTable('Genre');
    dropTable('Movie');
    dropTable('MovieCharacter');
    dropTable('Person');
END;
/

