-- <MAIN TABLES> ---------------------------------------------------------------------------------------------------------------------------

CREATE TABLE Genre (
    GenreID NUMBER(10) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    Genre   VARCHAR2(255) NOT NULL,
    CONSTRAINT pk_genre_id PRIMARY KEY (GenreID),
    CONSTRAINT uk_genre UNIQUE (Genre),
    CONSTRAINT ck_genre CHECK (Genre IN
                               ('Documentary', 'History', 'Action', 'Animation', 'Crime', 'Sci-Fi', 'Thriller', 'Adventure', 'Biography', 'Drama', 'Romance', 'Fantasy', 'Comedy', 'Horror'))
);

CREATE TABLE Movie (
    MovieID NUMBER(10) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    Title   VARCHAR2(255) NOT NULL,
    Year    NUMBER(4)     NOT NULL,
    Type    CHAR          NOT NULL,
    CONSTRAINT pk_movie_id PRIMARY KEY (MovieID)
);


CREATE TABLE MovieCharacter (
    MovCharID NUMBER(10) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    Movie     NUMBER(10)    NOT NULL,
    Character VARCHAR2(255) NOT NULL,
    Alias     VARCHAR2(255),
    Position  NUMBER(3),
    CONSTRAINT pk_movie_char_id PRIMARY KEY (MovCharID),
    CONSTRAINT pk_movie_char_movie FOREIGN KEY (Movie) REFERENCES Movie (MovieID)
);


CREATE TABLE Person (
    PersonID NUMBER(10) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    Name     VARCHAR2(255) NOT NULL,
    Sex      CHAR          NOT NULL,
    CONSTRAINT pk_person_id PRIMARY KEY (PersonID),
    CONSTRAINT uk_person_name UNIQUE (Name),
    CONSTRAINT ck_person_sex CHECK (Sex IN ('M', 'W'))
);

-- </MAIN TABLES> --------------------------------------------------------------------------------------------------------------------------

-- <RELATIONS> -----------------------------------------------------------------------------------------------------------------------------

CREATE TABLE MovieGenre (
    Movie NUMBER(10) NOT NULL,
    Genre NUMBER(10) NOT NULL,
    CONSTRAINT fk_movie_genre_movie FOREIGN KEY (Movie) REFERENCES Movie (MovieID),
    CONSTRAINT fk_movie_genre_genre FOREIGN KEY (Genre) REFERENCES Genre (GenreID)
);

CREATE TABLE PersonPlays (
    Person    NUMBER(10) NOT NULL,
    MovieChar NUMBER(10) NOT NULL,
    CONSTRAINT fk_person_plays_person FOREIGN KEY (Person) REFERENCES Person (PersonID),
    CONSTRAINT fk_person_plays_movie_char FOREIGN KEY (MovieChar) REFERENCES MovieCharacter (MovCharID)
);

